package javabasics;

public class Loops 
{

	public static void main(String[] args)
	{
		
		 int i=10;
		while(i>=1)
		{
			System.out.println(i);
			i--; 
		}
			
			System.out.println("@@@@@@@@@ for loop @@@@@@@@@@@@@@");
			
			
	for(int k=10;k>=-10;k--)
		{
			System.out.println(k);
		}
	
	}
}

		

